#include <stdio.h>

int main()
{
    int a=19876,b=0675;
    printf("The Hexadecimal value of Decimal number %d is %x \n",a,a);
    printf("The Octal value of Decimal number %d is %o \n",a,a);
    printf("The Hexadecimal value of Octal number %o is %x \n",b,b);
    printf("The Decimal value of Octal number %o is %d",b,b);


    return 0;
}